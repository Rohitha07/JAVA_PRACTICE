public class add {
    public static void main(String arg[])
    {
        // General Rule -> datatype variableName = valueStoredInVariable
        int a = 10;
        int b = 20;
        
        // To add two integers we can use + operator 
        int c = a+b;
        
        // To print value of a variable we will not write that in double quote 
        System.out.println(c);
    }
}
