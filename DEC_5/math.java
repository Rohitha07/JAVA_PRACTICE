public class math {
    public static void main(String arg[])
    {
        int a = 1042;
        int b = 7;
        
        // / (Division symbol -> Quotient)
        // % (Modulus symbol -> Remainder)
        
        int quo = a/b; // Quotient
        int rem = a%b; // Remainder
        
        // Combining english sentence with a variable using +
        System.out.println(quo + " " + rem);
    }
}
